export interface CreatureAttack {
  name: string;
  range: string;
  formula: string;
  damage: string;
  rounds: string;
}

export interface CreatureAbility {
  name: string;
  description: string;
  linkEntryId?: string;
}

export interface CreatureEquipmentItem {
  name: string;
  linkEntryId?: string;
}

export interface CreatureStatBlock {
  characteristics: { code: string; value: number }[];
  speed: string;
  wounds: number;
  type: string;
  skills: string[];
  attacks: CreatureAttack[];
  defenses: string[];
  abilities: CreatureAbility[];
  equipment?: CreatureEquipmentItem[];
}

export interface OpinionQuote {
  quote: string;
  author: string;
  linkEntryId?: string;
}

export interface Callout {
  title: string;
  items: string[];
}

export interface StatLink {
  // Точная подстрока в значении характеристики, которая станет ссылкой
  match: string;
  // Либо ссылка на конкретную запись кодекса...
  entryId?: string;
  // ...либо ссылка, открывающая окно со всеми записями раздела (например «Происхождения»)
  sectionId?: SectionId;
}

export interface StatRowValue {
  label: string;
  value: string;
  links?: StatLink[];
}

export interface CodexEntry {
  id: string;
  title: string;
  section: SectionId;
  source: SourceId;
  summary: string;
  tags: string[];
  meta?: string;
  category?: ItemCategoryId;
  stats?: StatRowValue[];
  subgroup?: string;
  creatureStats?: CreatureStatBlock;
  portrait?: string;
  otherOpinions?: OpinionQuote[];
  relatedEntryIds?: string[];
  callouts?: Callout[];
  // Ссылки на подстроки внутри текста summary (аналогично links в StatRowValue)
  summaryLinks?: StatLink[];
}

